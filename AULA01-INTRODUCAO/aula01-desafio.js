//Construa funções para somar, subtrair, dividir e multiplicar, mas cada uma delas só pode ocupar uma única linha. 

const soma = (num1, num2) => num1+num2

const subtrair = (num1, num2) => num1-num2

const dividir = (num1, num2) => num1/num2

const multiplicar = (num1, num2) => num1*num2

console.log("Soma: "+soma(1,1))
console.log("Subtração: "+subtrair(2,1))
console.log("Dividir: "+dividir(15,3))
console.log("Multiplicação: " +multiplicar(5,5))